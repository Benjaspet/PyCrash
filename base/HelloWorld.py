# For any programming languages, the first program you will see at the most
# basic level will be a one-lined program named "Hello world", when executed,
# will print the string "Hello world" in the console. In Python, this can be done
# via the "print" function:

print("Hello world")

# You can also print multiple items in the console by passing more parameters
# into the base function:

print("Hello world", "Hello Bob", "Goodbye Steve")

# An important note for Python: unlike traditional programming, semicolons at the
# end of each line are not needed and will throw an error in your IDE if they
# are added.